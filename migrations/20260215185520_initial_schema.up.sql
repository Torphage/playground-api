);

COMMENT ON TABLE identity.permissions IS 'Permissions are used to define what actions a user can perform. Any value in this table must also have a hard-coded representation in code.';
COMMENT ON COLUMN identity.permissions.slug IS 'Unique identifier (e.g., "users:read"). Used in code.';

CREATE TRIGGER audit_identity_permissions
    AFTER UPDATE
    ON identity.permissions
    FOR EACH ROW
