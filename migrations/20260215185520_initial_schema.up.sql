-- 2. AUDIT LOGGING (The "Black Box")
-- ==========================================

-- The Central Log Table
CREATE TABLE audit.logs
(
    id          integer PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    schema_name text        NOT NULL,
    table_name  text        NOT NULL,
    record_id   jsonb       NOT NULL, -- Flexible ID storage
    operation   text        NOT NULL, -- 'INSERT', 'UPDATE', 'DELETE'
    old_data    jsonb,
    new_data    jsonb,
    changed_by  uuid,                 -- Set via "SET LOCAL app.current_user_id = '...'"
    changed_at  timestamptz NOT NULL DEFAULT now()
);

COMMENT ON TABLE audit.logs IS 'Centralized audit log table. Only used for tables that does not have their own dedicated shadow table.';
COMMENT ON COLUMN audit.logs.record_id IS 'Flexible ID storage. Can be a UUID, an integer, a slug, or a JSON object representing a composite key.';

CREATE INDEX idx_audit_record ON audit.logs (table_name, record_id);
CREATE INDEX idx_audit_record_id ON audit.logs USING GIN (record_id);
CREATE INDEX idx_audit_time ON audit.logs (changed_at);

-- The Generic Trigger Function
CREATE OR REPLACE FUNCTION audit.log_change() RETURNS TRIGGER AS
$$
DECLARE
    current_user_id UUID;

    -- Variables for dynamic key extraction
    key_col         TEXT;
    key_object      JSONB := '{}'::jsonb; -- Start with an empty object
    row_data        JSONB; -- Holds either NEW or OLD data
    i               INTEGER;
BEGIN
    -- 1. Get User ID
    BEGIN
        current_user_id := current_setting('app.current_user_id')::UUID;
    EXCEPTION
        WHEN OTHERS THEN
            current_user_id := NULL;
    END;

    -- 2. Determine which row data to use for ID extraction
    -- For DELETE, we must use OLD. For INSERT/UPDATE, we use NEW.
    IF (TG_OP = 'DELETE') THEN
        row_data := to_jsonb(OLD);
    ELSE
        row_data := to_jsonb(NEW);
    END IF;

    -- 3. Build the Composite Key ID
    -- If no arguments are provided, default to 'id'
    IF TG_NARGS = 0 THEN
        key_object := jsonb_build_object('id', row_data ->> 'id');
    ELSE
        -- Loop through all arguments (column names) passed to the trigger
        FOR i IN 0 .. (TG_NARGS - 1)
            LOOP
                key_col := TG_ARGV[i];
                -- Add { "col_name": "value" } to our key object
                key_object := key_object || jsonb_build_object(key_col, row_data ->> key_col);
            END LOOP;
    END IF;

    -- 4. Insert the Log
    IF (TG_OP = 'DELETE') THEN
        INSERT INTO audit.logs (schema_name, table_name, record_id, operation, old_data, changed_by)
        VALUES (TG_TABLE_SCHEMA, TG_TABLE_NAME, key_object, 'DELETE', to_jsonb(OLD), current_user_id);
        RETURN OLD;

    ELSIF (TG_OP = 'UPDATE') THEN
        IF (OLD IS NOT DISTINCT FROM NEW) THEN RETURN NEW; END IF;

        INSERT INTO audit.logs (schema_name, table_name, record_id, operation, old_data, new_data, changed_by)
        VALUES (TG_TABLE_SCHEMA, TG_TABLE_NAME, key_object, 'UPDATE', to_jsonb(OLD), to_jsonb(NEW), current_user_id);
        RETURN NEW;

    ELSIF (TG_OP = 'INSERT') THEN
        INSERT INTO audit.logs (schema_name, table_name, record_id, operation, new_data, changed_by)
        VALUES (TG_TABLE_SCHEMA, TG_TABLE_NAME, key_object, 'INSERT', to_jsonb(NEW), current_user_id);
        RETURN NEW;
    END IF;

    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

-- ==========================================
-- 3. IDENTITY SCHEMA (User and Role Management)
-- ==========================================

--- IDENTITY.USERS ---

CREATE TABLE identity.users
(
    id            uuid PRIMARY KEY, -- Generated v7 in Axum
    username      text        NOT NULL,
    email         text        NOT NULL,
    password_hash text        NOT NULL,
    created_at    timestamptz NOT NULL DEFAULT now(),
    updated_at    timestamptz NOT NULL DEFAULT now()
);

COMMENT ON TABLE identity.users IS 'Global user registry for the entire Super-App.';
COMMENT ON COLUMN identity.users.id IS 'UUID v7 generated by the application for time-sortable indexing.';

CREATE TRIGGER audit_identity_users
    AFTER UPDATE
    ON identity.users
    FOR EACH ROW
EXECUTE FUNCTION audit.log_change();

--- IDENTITY.ROLES ---

CREATE TABLE identity.roles
(
    id   text PRIMARY KEY,    -- e.g. "admin", "editor"
    name text NOT NULL UNIQUE -- e.g. "Administrator"
);

COMMENT ON TABLE identity.roles IS 'Roles are used to group permissions together. A user can have multiple roles.';
COMMENT ON COLUMN identity.roles.id IS 'Unique identifier (e.g., "admin"). Used in code.';

CREATE TRIGGER audit_identity_roles
    AFTER UPDATE
    ON identity.roles
    FOR EACH ROW
EXECUTE FUNCTION audit.log_change();

--- IDENTITY.USERS_ROLES ---

-- Users can have multiple roles
CREATE TABLE identity.user_roles
(
    user_id uuid REFERENCES identity.users (id),
    role_id text REFERENCES identity.roles (id),
    PRIMARY KEY (user_id, role_id)
);

CREATE TRIGGER audit_identity_user_roles
    AFTER UPDATE
    ON identity.user_roles
    FOR EACH ROW
EXECUTE FUNCTION audit.log_change('user_id', 'role_id');

--- IDENTITY.PERMISSIONS ---

CREATE TABLE identity.permissions
(
    slug        text PRIMARY KEY, -- e.g. "kitchen.recipe.create"
